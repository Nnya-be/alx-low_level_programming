#include "3-calc.h"
/**
 *op_add - function to add
 *@a : first number
 *@b : second number
 *Return: always 0
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 *op_sub - function to subtract
 *@a : first number
 *@b : second number
 *Return: always 0
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 *op_mul - function to multiply
 *@a : first number
 *@b : second number
 *Return: always 0
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 *op_div - function to divide
 *@a : first number
 *@b : second number
 *Return: always 0
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 *op_mod - function to perform modulos
 *@a : first number
 *@b : second number
 *Return: always 0
 */
int op_mod(int a, int b)
{
return (a % b);
}
